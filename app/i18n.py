def translate_text(value: str, language: str) -> str:
    if normalize_language(language) != "en" or not value:
        return value
    leading = value[: len(value) - len(value.lstrip())]
    trailing = value[len(value.rstrip()) :]
    core = value.strip()
    if not core:
        return value
    exact = EXACT_EN.get(core)
    if exact is not None:
        return leading + exact + trailing
    translated = core
    for source, target in PHRASE_EN:
        pattern = r"(?<![\wÄÖÜäöüß])" + re.escape(source) + r"(?![\wÄÖÜäöüß])"
        translated = re.sub(pattern, lambda _match, replacement=target: replacement, translated)
    return leading + translated + trailing


class _HTMLTranslator(HTMLParser):
    SKIP_TAGS = {"script", "style", "pre", "code", "textarea"}
    TRANSLATED_ATTRS = {"title", "placeholder", "aria-label", "data-label", "alt"}

    def __init__(self, language: str):
        super().__init__(convert_charrefs=False)
        self.language = language
        self.parts: List[str] = []
        self.skip_depth = 0
        self.skip_tags: List[str] = []

    @staticmethod
    def _attrs(attrs: Iterable[Tuple[str, str | None]]) -> str:
        rendered: List[str] = []
        for name, value in attrs:
            if value is None:
                rendered.append(name)
            else:
                rendered.append(f'{name}="{html.escape(value, quote=True)}"')
        return (" " + " ".join(rendered)) if rendered else ""

    def handle_decl(self, decl: str) -> None:
        self.parts.append(f"<!{decl}>")

    def handle_starttag(self, tag: str, attrs: List[Tuple[str, str | None]]) -> None:
        attr_map = {name.lower(): (value or "").lower() for name, value in attrs}
        skip_requested = (
            tag in self.SKIP_TAGS
            or attr_map.get("translate") == "no"
            or attr_map.get("data-i18n") == "skip"
        )
        translated_attrs: List[Tuple[str, str | None]] = []
        for name, value in attrs:
            if value is not None and name in self.TRANSLATED_ATTRS and not (self.skip_depth or skip_requested):
                value = translate_text(value, self.language)
            if tag == "html" and name == "lang":
                value = self.language
            translated_attrs.append((name, value))
        self.parts.append(f"<{tag}{self._attrs(translated_attrs)}>")
        if skip_requested:
            self.skip_depth += 1
            self.skip_tags.append(tag)

    def handle_startendtag(self, tag: str, attrs: List[Tuple[str, str | None]]) -> None:
        attr_map = {name.lower(): (value or "").lower() for name, value in attrs}
        skip_requested = attr_map.get("translate") == "no" or attr_map.get("data-i18n") == "skip"
        translated_attrs: List[Tuple[str, str | None]] = []
        for name, value in attrs:
            if value is not None and name in self.TRANSLATED_ATTRS and not (self.skip_depth or skip_requested):
                value = translate_text(value, self.language)
            translated_attrs.append((name, value))
        self.parts.append(f"<{tag}{self._attrs(translated_attrs)}>")

    def handle_endtag(self, tag: str) -> None:
        self.parts.append(f"</{tag}>")
        if self.skip_tags and tag == self.skip_tags[-1]:
            self.skip_tags.pop()
            self.skip_depth = max(0, self.skip_depth - 1)

    def handle_data(self, data: str) -> None:
        self.parts.append(data if self.skip_depth else translate_text(data, self.language))

    def handle_entityref(self, name: str) -> None:
        self.parts.append(f"&{name};")

    def handle_charref(self, name: str) -> None:
        self.parts.append(f"&#{name};")

    def handle_comment(self, data: str) -> None:
        self.parts.append(f"<!--{data}-->")

    def handle_pi(self, data: str) -> None:
        self.parts.append(f"<?{data}>")

    def unknown_decl(self, data: str) -> None:
        self.parts.append(f"<![{data}]>")


def translate_html(document: str, language: str) -> str:
    if normalize_language(language) != "en" or not document:
        return document
    parser = _HTMLTranslator("en")
    try:
        parser.feed(document)
        parser.close()
        return "".join(parser.parts)
    except Exception:
        # Translation must never make a functional page unavailable.
        return document
