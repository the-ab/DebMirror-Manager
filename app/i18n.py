SUPPORTED_LANGUAGES = {"de": "Deutsch", "en": "English"}
SUPPORTED_APPEARANCES = {"light", "dark", "auto"}


def normalize_language(value: str | None, default: str = "de") -> str:
    candidate = str(value or "").strip().lower().replace("_", "-")
    if candidate.startswith("en"):
        return "en"
    if candidate.startswith("de"):
        return "de"
    return default if default in SUPPORTED_LANGUAGES else "de"


def normalize_appearance(value: str | None, default: str = "light") -> str:
    candidate = str(value or "").strip().lower()
    return candidate if candidate in SUPPORTED_APPEARANCES else default


